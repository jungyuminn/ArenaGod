SILVER_AUGMENT_DETAILS = {
    name: data for name, data in AUGMENT_DETAILS.items() if data["tier"] == "silver"
}

GOLD_AUGMENT_DETAILS = {
    name: data for name, data in AUGMENT_DETAILS.items() if data["tier"] == "gold"
}

PRISMATIC_AUGMENT_DETAILS = {
    name: data for name, data in AUGMENT_DETAILS.items() if data["tier"] == "prismatic"
}

def get_augment_details(name, tier=None):
    """
    증강 상세 정보를 가져옵니다.
    
    Args:
        name (str): 증강 이름
        tier (str, optional): 증강 등급 ("silver", "gold", "prismatic"). None이면 모든 등급에서 검색
    
    Returns:
        dict or None: 증강 상세 정보. 찾지 못하면 None 반환
    """
    if tier == "silver":
        return SILVER_AUGMENT_DETAILS.get(name)
    elif tier == "gold":
        return GOLD_AUGMENT_DETAILS.get(name)
    elif tier == "prismatic":
        return PRISMATIC_AUGMENT_DETAILS.get(name)
    else:
        # tier가 None이면 모든 등급에서 검색
        return (SILVER_AUGMENT_DETAILS.get(name) or 
                GOLD_AUGMENT_DETAILS.get(name) or 
                PRISMATIC_AUGMENT_DETAILS.get(name))
